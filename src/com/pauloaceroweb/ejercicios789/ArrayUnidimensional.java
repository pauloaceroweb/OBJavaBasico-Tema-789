package com.pauloaceroweb.ejercicios789;

public class ArrayUnidimensional {

    public static void main(String[] args) {

        String[] nombres = {
                "Daniel",
                "Valerie",
                "Liliana",
                "Mariela",
                "Fernando"
        };

        for (String nombre : nombres) {
            System.out.println("Nombres de mi Familia: " + nombre);
        }
    }
}
